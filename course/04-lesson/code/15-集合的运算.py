# 在对集合做运算时，不会影响原来的集合，而是返回一个运算结果
# 创建两个集合
s = {1,2,3,4,5,6}
s2 = {4,5,6,7,8}

#  & 交集运算
result=s & s2

  # | 并集运算
result = s | s2    #

# - 差集
result = s - s2    #result= {1, 2, 3}

#  ^  异或集
result = s ^ s2 #result= {1, 2, 3, 7, 8}

#  <= 检查一个集合是否是另一个集合的子集
# 如果一个集合中的元素全部都在另一个集合中出现，那么这个集合就是另一个集合的子集
a = {1,2,4,}
b = {1,2,4,5,6}
result = a <= b


# <检查一个集合是否是另一个集合真子集
a = {1,2,4,}
b = {1,2,4,5,6}
result = a < b
print("result=",result)